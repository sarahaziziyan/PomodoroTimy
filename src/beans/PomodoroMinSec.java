package beans;

public class PomodoroMinSec {
    
    private int pomodoroMin;
    private int pomodoroSec;

    public PomodoroMinSec(int pomodoroMin, int pomodoroSec) {
        this.pomodoroMin = pomodoroMin;
        this.pomodoroSec = pomodoroSec;
    }

    public int getPomodoroMin() {
        return pomodoroMin;
    }

    public void setPomodoroMin(int pomodoroMin) {
        this.pomodoroMin = pomodoroMin;
    }

    public int getPomodoroSec() {
        return pomodoroSec;
    }

    public void setPomodoroSec(int pomodoroSec) {
        this.pomodoroSec = pomodoroSec;
    }
    
    
    
}
